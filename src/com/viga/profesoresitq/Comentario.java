package com.viga.profesoresitq;

public class Comentario {

	private String materia;
	private String facilidad;
	private String ayuda;
	private String claridad;
	private String comentario;
	private String recomendado;
	private String fecha;
	
	public void setMateria(String materia) {
		this.materia = materia;
	}
	public String getMateria() {
		return this.materia;
	}
	
	public void setFacilidad(String facilidad) {
		this.facilidad = facilidad;
	}
	public String getFacilidad() {
		return this.facilidad;
	}
	
	public void setClaridad(String claridad) {
		this.claridad = claridad;
	}
	public String getClaridad() {
		return this.claridad;
	}
	
	public void setComentario(String comentario) {
		this.comentario = comentario;
	}
	public String getComentario() {
		return this.comentario;
	}
	
	public void setAyuda(String ayuda) {
		this.ayuda = ayuda;
	}
	public String getAyuda() {
		return this.ayuda;
	}
	
	public void setRecomendado(String recomendado) {
		this.recomendado = recomendado;
	}
	public String getRecomendado() {
		return this.recomendado;
	}
	
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	public String getFecha() {
		return this.fecha;
	}
	
}
